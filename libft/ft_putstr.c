#include "libft.h"

int	ft_putstr(char *s)
{
	int	c;

	c = 0;
	if (s)
	{
		while (*s != '\0')
		{
			c = c + write(1, s, 1);
			s++;
		}
	}
	else
		c = c + write(1, "(null)", 6);
	return (c);
}
