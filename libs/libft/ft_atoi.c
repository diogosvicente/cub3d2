#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	signal;
	int	result;

	i = 0;
	signal = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signal = signal * (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (str[i] -48) + (result * 10);
		i++;
	}
	return (signal * result);
}
